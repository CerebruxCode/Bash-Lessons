do
    case $opt in
        "Επιλογή 1")
		echo "Παίχτε Πανκ"
		;;
	"Επιλογή 2")
		echo "Μην Παραχαράσετε..."
		;;
	"Επιλογή 3")
		echo "Πόσιμπολ"
		;;
	"Έξοδος")
		echo "Έξοδος..."
		exit 0
		;;
	*)
